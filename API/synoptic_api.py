# ── Write all synoptic variables to their PI tags ─────────────────────────────
def write_synoptic_to_pi(df, tag_map, time_col="date", update_option="Replace"):
    """
    Write each weather variable in df to its corresponding PI tag.
    df       : your df_weather DataFrame
    tag_map  : dict mapping df column names to PI tag names
    time_col : the datetime column to use as the PI timestamp
    """
    results = {}
    for col, tag_name in tag_map.items():
        if col not in df.columns:
            print(f"Skipping {col} — column not found in DataFrame")
            continue

        print(f"\n{'='*60}")
        print(f"Writing {col} → {tag_name}")

        r = write_recorded_values(
            tag_name=tag_name,
            rows=df,
            time_col=time_col,
            value_col=col,
            update_option=update_option
        )
        results[col] = r

    print(f"\n{'='*60}")
    print("Done. Summary:")
    for col, r in results.items():
        if r is None:
            print(f"  {col}: skipped or dry run")
        else:
            print(f"  {col}: {r.status_code}")

    return results

