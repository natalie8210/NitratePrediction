# ── Pull Data ─────────────────────────────────────────────────────────────────
def pull_synoptic_data(station_id, start, end, token, vars_str):
    params = {
        "token":  token,
        "stid": "COOPICYI4",
        "start":  start,
        "end":    end,
        "vars":   vars_str,
        "units":  "english",   # change to "metric" if preferred
        "output": "json"
    }

    print(f"Requesting data for station: {station_id}")
    r = requests.get(BASE_URL, params=params, timeout=60)
    print(f"Status: {r.status_code}")
    print(f"URL: {r.url}")
    r.raise_for_status()

    data = r.json()

    # Check API response code
    summary = data.get("SUMMARY", {})
    if summary.get("RESPONSE_CODE") != 1:
        raise ValueError(f"API error: {summary.get('RESPONSE_MESSAGE')}")

    stations = data.get("STATION", [])
    if not stations:
        raise ValueError("No stations returned.")

    all_dfs = []

    for station in stations:
        stid = station["STID"]
        obs  = station["OBSERVATIONS"]

        # Build base DataFrame from timestamps
        df = pd.DataFrame({"datetime": pd.to_datetime(obs["date_time"])})

        # Map expected set_1 keys to clean column names
        var_map = {
          "air_temp_set_1":               "air_temp",
          "snow_depth_set_1":             "snow_depth",
          "snow_accum_24_hour_set_1":     "snow_accum",
          "precip_accum_24_hour_set_1":   "precip_accum",
          "air_temp_high_24_hour_set_1":  "air_temp_high",
          "air_temp_low_24_hour_set_1":   "air_temp_low"
        }

        for raw_key, clean_name in var_map.items():
            if raw_key in obs:
                df[clean_name] = obs[raw_key]
            else:
                # Try set_1d (derived variables use this suffix)
                derived_key = raw_key.replace("_set_1", "_set_1d")
                if derived_key in obs:
                    df[clean_name] = obs[derived_key]
                else:
                    print(f"  Warning: {raw_key} not found for station {stid}")
                    df[clean_name] = None

        df.insert(0, "stid", stid)
        all_dfs.append(df)

    result = pd.concat(all_dfs, ignore_index=True)
    return result


